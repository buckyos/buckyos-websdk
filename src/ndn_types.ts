export const DEFAULT_HASH_METHOD = 'sha256'

export type HashMethod =
    | 'sha256'
    | 'sha512'
    | 'qcid'
    | 'blake2s256'
    | 'keccak256'

export const HashMethod = {
    Sha256: 'sha256' as HashMethod,
    Sha512: 'sha512' as HashMethod,
    QCID: 'qcid' as HashMethod,
    Blake2s256: 'blake2s256' as HashMethod,
    Keccak256: 'keccak256' as HashMethod,

    hashResultSize(method: HashMethod): number {
        switch (method) {
            case 'sha256': return 32
            case 'sha512': return 64
            case 'qcid': return 32
            case 'blake2s256': return 32
            case 'keccak256': return 32
        }
    },

    /** parse(s) -> [HashMethod, isMix]; "mix256" -> [Sha256, true]. */
    parse(s: string): [HashMethod, boolean] {
        const isMix = s.startsWith('mix')
        const method = HashMethod.fromString(s)
        return [method, isMix]
    },

    fromString(s: string): HashMethod {
        switch (s) {
            case 'sha256':
            case 'mix256':
                return 'sha256'
            case 'sha512':
            case 'mix512':
                return 'sha512'
            case 'qcid':
            case 'mixqcid':
                return 'qcid'
            case 'blake2s256':
            case 'mixblake2s256':
                return 'blake2s256'
            case 'keccak256':
            case 'mixkeccak256':
                return 'keccak256'
            default:
                throw new NdnError('InvalidData', `Invalid hash method: ${s}`)
        }
    },
}

