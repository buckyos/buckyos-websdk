/**
 * Anything that can be hashed into an ObjId. Implementations need only
 * provide getObjType() and toJSON() (the JSON-serializable form). The
 * default genObjId() then derives the ObjId from canonical JSON.
 */
export interface NamedObject {
    getObjType(): string
    toJSON(): unknown
    genObjId(): [ObjId, string]
}

export abstract class NamedObjectBase implements NamedObject {
    abstract getObjType(): string
    abstract toJSON(): unknown
    genObjId(): [ObjId, string] {
        return buildNamedObjectByJson(this.getObjType(), this.toJSON())
    }
}

/** SHA-256-based ObjId derivation: matches build_obj_id() in ndn-lib. */
export function buildObjId(objType: string, objJsonStr: string): ObjId {
    const hash = sha256Utf8(objJsonStr)
    return ObjId.fromRaw(objType, hash)
}

/**
 * Canonicalize the JSON value (recursive sort), serialize, then derive ObjId.
 * Matches build_named_object_by_json() in ndn-lib.
 */
export function buildNamedObjectByJson(
    objType: string,
    jsonValue: unknown,
): [ObjId, string] {
    const jsonStr = toCanonicalJsonString(jsonValue)
    const objId = buildObjId(objType, jsonStr)
    return [objId, jsonStr]
}

/** Verify that the JSON canonical hash equals the expected ObjId. */
export function verifyNamedObject(objId: ObjId, jsonValue: unknown): boolean {
    const [objId2] = buildNamedObjectByJson(objId.objType, jsonValue)
    return objId.equals(objId2)
}

export function verifyNamedObjectFromStr(objId: ObjId, objStr: string): unknown {
    let parsed: unknown
    try {
        parsed = JSON.parse(objStr)
    } catch (e) {
        throw new NdnError('InvalidId', `failed to parse obj_str: ${(e as Error).message}`)
    }
    if (!verifyNamedObject(objId, parsed)) {
        throw new NdnError('InvalidId', `verify named object failed: ${objStr}`)
    }
    return parsed
}

/**
 * Parse an obj-data-string. The Rust version also accepts JWT, but here we
 * just look at whether the string starts with `{`. JWT decoding is left to
 * the caller (it depends on signature verification anyway).
 */
export function loadNamedObjectFromObjStr(objStr: string): unknown {
    if (objStr.indexOf('{') >= 0) {
        try {
            return JSON.parse(objStr)
        } catch (e) {
            throw new NdnError('InvalidId', `failed to parse obj_str: ${(e as Error).message}`)
        }
    }
    throw new NdnError(
        'Unsupported',
        'JWT-encoded named objects are not supported in this TypeScript port',
    )
}

export function loadNamedObj<T = unknown>(objStr: string): T {
    return loadNamedObjectFromObjStr(objStr) as T
}

export function loadNamedObjAndVerify<T = unknown>(objId: ObjId, objStr: string): T {
    const parsed = loadNamedObjectFromObjStr(objStr)
    if (!verifyNamedObject(objId, parsed)) {
        throw new NdnError('InvalidId', `verify named object failed for obj_id: ${objId.toString()}`)
    }
    return parsed as T
}

/** Walk a JSON value via a `/foo/bar/0/baz` path and parse it as ObjId. */
export function extractObjIdByPath(jsonValue: unknown, path: string): ObjId {
    const parts = path.split('/').filter(p => p.length > 0)
    let cursor: any = jsonValue
    for (const p of parts) {
        if (cursor == null) {
            throw new NdnError('InvalidParam', `objid path not found: ${path}`)
        }
        if (Array.isArray(cursor)) {
            const idx = Number(p)
            if (!Number.isInteger(idx) || idx < 0 || idx >= cursor.length) {
                throw new NdnError('InvalidParam', `objid path not found: ${path}`)
            }
            cursor = cursor[idx]
        } else if (typeof cursor === 'object') {
            if (!(p in cursor)) {
                throw new NdnError('InvalidParam', `objid path not found: ${path}`)
            }
            cursor = cursor[p]
        } else {
            throw new NdnError('InvalidParam', `objid path not found: ${path}`)
        }
    }
    try {
        return ObjId.fromValue(cursor)
    } catch (e) {
        throw new NdnError('InvalidData', `invalid objid at path ${path}: ${(e as Error).message}`)
    }
}

