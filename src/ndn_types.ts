const HEX_CHARS = '0123456789abcdef'

export function bytesToHex(bytes: Uint8Array): string {
    let s = ''
    for (let i = 0; i < bytes.length; i++) {
        const b = bytes[i]
        s += HEX_CHARS[(b >>> 4) & 0xf]
        s += HEX_CHARS[b & 0xf]
    }
    return s
}

export function hexToBytes(hex: string): Uint8Array {
    if (hex.length % 2 !== 0) {
        throw new NdnError('InvalidId', `invalid hex length: ${hex.length}`)
    }
    const out = new Uint8Array(hex.length / 2)
    for (let i = 0; i < out.length; i++) {
        const hi = HEX_CHARS.indexOf(hex[i * 2].toLowerCase())
        const lo = HEX_CHARS.indexOf(hex[i * 2 + 1].toLowerCase())
        if (hi < 0 || lo < 0) {
            throw new NdnError('InvalidId', `invalid hex char at offset ${i * 2}`)
        }
        out[i] = (hi << 4) | lo
    }
    return out
}

// Base32 RFC4648 lowercase, no padding (matches base32::Alphabet::Rfc4648Lower
// {padding: false} used by ndn-lib).
const BASE32_ALPHABET = 'abcdefghijklmnopqrstuvwxyz234567'

export function base32Encode(bytes: Uint8Array): string {
    let bits = 0
    let value = 0
    let out = ''
    for (let i = 0; i < bytes.length; i++) {
        value = (value << 8) | bytes[i]
        bits += 8
        while (bits >= 5) {
            bits -= 5
            out += BASE32_ALPHABET[(value >>> bits) & 0x1f]
        }
    }
    if (bits > 0) {
        out += BASE32_ALPHABET[(value << (5 - bits)) & 0x1f]
    }
    return out
}

export function base32Decode(str: string): Uint8Array {
    const lower = str.toLowerCase()
    let bits = 0
    let value = 0
    const out: number[] = []
    for (let i = 0; i < lower.length; i++) {
        const ch = lower[i]
        const idx = BASE32_ALPHABET.indexOf(ch)
        if (idx < 0) {
            throw new NdnError('InvalidId', `invalid base32 char '${ch}' at ${i}`)
        }
        value = (value << 5) | idx
        bits += 5
        if (bits >= 8) {
            bits -= 8
            out.push((value >>> bits) & 0xff)
        }
    }
    return new Uint8Array(out)
}

// Unsigned varint (LEB128) encoding, matches `unsigned_varint` crate used by
// ndn-lib for length encoding inside ChunkId/ChunkList.
export function varintEncode(value: number): Uint8Array {
    if (!Number.isFinite(value) || value < 0) {
        throw new NdnError('InvalidParam', `varint must be non-negative finite: ${value}`)
    }
    if (!Number.isSafeInteger(value)) {
        throw new NdnError('InvalidParam', `varint exceeds safe integer range: ${value}`)
    }
    const out: number[] = []
    let v = value
    while (v >= 0x80) {
        out.push((v & 0x7f) | 0x80)
        v = Math.floor(v / 128)
    }
    out.push(v & 0x7f)
    return new Uint8Array(out)
}

// Returns [value, bytes_consumed]
export function varintDecode(bytes: Uint8Array, offset: number = 0): [number, number] {
    let result = 0
    let shiftMul = 1
    let i = offset
    let consumed = 0
    while (i < bytes.length) {
        const b = bytes[i++]
        consumed++
        const part = b & 0x7f
        // result += part * 2^(7*consumed-7)
        result += part * shiftMul
        if (!Number.isSafeInteger(result)) {
            throw new NdnError('InvalidData', 'varint exceeds safe integer range')
        }
        if ((b & 0x80) === 0) {
            return [result, consumed]
        }
        shiftMul *= 128
        if (consumed > 10) {
            throw new NdnError('InvalidData', 'varint too long')
        }
    }
    throw new NdnError('InvalidData', 'varint truncated')
}

