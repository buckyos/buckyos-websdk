export function sha256Bytes(data: Uint8Array): Uint8Array {
    const sha = new jsSHA('SHA-256', 'UINT8ARRAY')
    sha.update(data)
    return sha.getHash('UINT8ARRAY')
}

export function sha256Utf8(text: string): Uint8Array {
    const sha = new jsSHA('SHA-256', 'TEXT', { encoding: 'UTF8' })
    sha.update(text)
    return sha.getHash('UINT8ARRAY')
}

/**
 * Recursively sort object keys (BTreeMap-like). Arrays preserve order.
 * This matches the `stabilize_json` helper in ndn-lib so that the resulting
 * JSON string (and thus the SHA-256-derived ObjId) is deterministic.
 */
export function canonicalizeJson(value: any): any {
    if (value === null || typeof value !== 'object') return value
    if (Array.isArray(value)) return value.map(canonicalizeJson)
    const keys = Object.keys(value).sort()
    const result: Record<string, any> = {}
    for (const k of keys) {
        result[k] = canonicalizeJson(value[k])
    }
    return result
}

export function toCanonicalJsonString(value: any): string {
    return JSON.stringify(canonicalizeJson(value))
}

