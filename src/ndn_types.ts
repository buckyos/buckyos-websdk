export type ObjectLinkData =
    | { kind: 'sameAs'; target: ObjId }
    | { kind: 'partOf'; target: ObjId; range: { start: number; end: number } }

export class RelationObject extends NamedObjectBase {
    public source: ObjId
    public relation: string
    public target: ObjId
    public body: Record<string, unknown>
    public iat: number | null
    public exp: number | null

    constructor(
        source: ObjId,
        relation: string,
        target: ObjId,
        body: Record<string, unknown> = {},
    ) {
        super()
        this.source = source
        this.relation = relation
        this.target = target
        this.body = body
        this.iat = null
        this.exp = null
    }

    /**
     * Reconstruct a RelationObject from its JSON form. `source`, `relation`,
     * `target`, `iat` and `exp` are reserved top-level fields; every other
     * key lands back in `body` (matching what `toJSON` spreads out), so the
     * decode → re-encode round-trip is byte-stable under canonical JSON
     * for any shape the TS class is capable of emitting.
     */
    static fromJSON(value: Record<string, unknown>): RelationObject {
        const reserved = new Set(['source', 'relation', 'target', 'iat', 'exp'])
        const body: Record<string, unknown> = {}
        for (const [k, v] of Object.entries(value)) {
            if (!reserved.has(k)) body[k] = v
        }
        const rel = new RelationObject(
            ObjId.fromString(String(value.source ?? '')),
            String(value.relation ?? ''),
            ObjId.fromString(String(value.target ?? '')),
            body,
        )
        rel.iat = typeof value.iat === 'number' ? value.iat : null
        rel.exp = typeof value.exp === 'number' ? value.exp : null
        return rel
    }

    static createByLinkData(source: ObjId, link: ObjectLinkData): RelationObject {
        switch (link.kind) {
            case 'sameAs':
                return new RelationObject(source, RELATION_TYPE_SAME, link.target)
            case 'partOf':
                return new RelationObject(
                    source,
                    RELATION_TYPE_PART_OF,
                    link.target,
                    { range: { start: link.range.start, end: link.range.end } },
                )
        }
    }

    getLinkData(): ObjectLinkData {
        switch (this.relation) {
            case RELATION_TYPE_SAME:
                return { kind: 'sameAs', target: this.target }
            case RELATION_TYPE_PART_OF: {
                const range = this.body.range as { start?: unknown; end?: unknown } | undefined
                if (!range || typeof range.start !== 'number' || typeof range.end !== 'number') {
                    throw new NdnError(
                        'InvalidLink',
                        `invalid range: ${JSON.stringify(this.body.range)}`,
                    )
                }
                return {
                    kind: 'partOf',
                    target: this.target,
                    range: { start: range.start, end: range.end },
                }
            }
            default:
                throw new NdnError('InvalidLink', `invalid relation: ${this.relation}`)
        }
    }

    getObjType(): string {
        return OBJ_TYPE_RELATION
    }

    toJSON(): Record<string, unknown> {
        const out: Record<string, unknown> = {
            source: this.source.toString(),
            relation: this.relation,
            target: this.target.toString(),
        }
        for (const [k, v] of Object.entries(this.body)) {
            out[k] = v
        }
        if (this.iat != null) out.iat = this.iat
        if (this.exp != null) out.exp = this.exp
        return out
    }
}

// ============================================================
// KnownStandardObject (from lib.rs)
// ============================================================

export type KnownStandardObject =
    | { kind: 'dir'; obj: DirObject; objStr: string }
    | { kind: 'file'; obj: FileObject; objStr: string }
    | { kind: 'chunkList'; obj: SimpleChunkList; objStr: string }

export const KnownStandardObject = {
    fromObjData(objId: ObjId, objData: string): KnownStandardObject {
        switch (objId.objType) {
            case OBJ_TYPE_DIR: {
                let parsed: unknown
                try {
                    parsed = JSON.parse(objData)
                } catch (e) {
                    throw new NdnError(
                        'InvalidParam',
                        `parse dir object from json failed: ${(e as Error).message}`,
                    )
                }
                return {
                    kind: 'dir',
                    obj: DirObject.fromJSON(parsed as Record<string, unknown>),
                    objStr: objData,
                }
            }
            case OBJ_TYPE_FILE: {
                let parsed: unknown
                try {
                    parsed = JSON.parse(objData)
                } catch (e) {
                    throw new NdnError(
                        'InvalidParam',
                        `parse file object from json failed: ${(e as Error).message}`,
                    )
                }
                return {
                    kind: 'file',
                    obj: FileObject.fromJSON(parsed as Record<string, unknown>),
                    objStr: objData,
                }
            }
            case OBJ_TYPE_CHUNK_LIST_SIMPLE: {
                return {
                    kind: 'chunkList',
                    obj: SimpleChunkList.fromJson(objData),
                    objStr: objData,
                }
            }
            default:
                throw new NdnError('InvalidParam', `Unknown object type: ${objId.objType}`)
        }
    },

    /** Return ObjIds (and optional inline obj-string) for the children. */
    getChildObjs(known: KnownStandardObject): Array<{ objId: ObjId; objStr: string | null }> {
        switch (known.kind) {
            case 'dir': {
                const out: Array<{ objId: ObjId; objStr: string | null }> = []
                for (const [, item] of known.obj.entries()) {
                    const [objId, objStr] = SimpleMapItem.getObjId(item)
                    out.push({ objId, objStr: objStr.length > 0 ? objStr : null })
                }
                return out
            }
            case 'file': {
                if (known.obj.content.length === 0) return []
                return [{ objId: ObjId.fromString(known.obj.content), objStr: null }]
            }
            case 'chunkList':
                return known.obj.body.map(c => ({ objId: c.toObjId(), objStr: null }))
        }
    },
}

// ============================================================
// UTF-8 helpers (browser+node)
// ============================================================

const _textEncoder: { encode(s: string): Uint8Array } | null =
    typeof TextEncoder !== 'undefined' ? new TextEncoder() : null
const _textDecoder: { decode(b: Uint8Array): string } | null =
    typeof TextDecoder !== 'undefined' ? new TextDecoder('utf-8') : null

function utf8Encode(s: string): Uint8Array {
    if (_textEncoder) return _textEncoder.encode(s)
    // Fallback (very small targets / older runtimes).
    const out: number[] = []
    for (let i = 0; i < s.length; i++) {
        let c = s.charCodeAt(i)
        if (c < 0x80) {
            out.push(c)
        } else if (c < 0x800) {
            out.push(0xc0 | (c >> 6), 0x80 | (c & 0x3f))
        } else if (c >= 0xd800 && c <= 0xdbff && i + 1 < s.length) {
            const c2 = s.charCodeAt(++i)
            const cp = 0x10000 + (((c & 0x3ff) << 10) | (c2 & 0x3ff))
            out.push(
                0xf0 | (cp >> 18),
                0x80 | ((cp >> 12) & 0x3f),
                0x80 | ((cp >> 6) & 0x3f),
                0x80 | (cp & 0x3f),
            )
        } else {
            out.push(0xe0 | (c >> 12), 0x80 | ((c >> 6) & 0x3f), 0x80 | (c & 0x3f))
        }
    }
    return new Uint8Array(out)
}

function utf8Decode(bytes: Uint8Array): string {
    if (_textDecoder) return _textDecoder.decode(bytes)
    let s = ''
    let i = 0
    while (i < bytes.length) {
        const b = bytes[i++]
        if (b < 0x80) {
            s += String.fromCharCode(b)
        } else if (b < 0xe0) {
            s += String.fromCharCode(((b & 0x1f) << 6) | (bytes[i++] & 0x3f))
        } else if (b < 0xf0) {
            s += String.fromCharCode(
                ((b & 0x0f) << 12) | ((bytes[i++] & 0x3f) << 6) | (bytes[i++] & 0x3f),
            )
        } else {
            const cp =
                ((b & 0x07) << 18) |
                ((bytes[i++] & 0x3f) << 12) |
                ((bytes[i++] & 0x3f) << 6) |
                (bytes[i++] & 0x3f)
            const u = cp - 0x10000
            s += String.fromCharCode(0xd800 + (u >> 10), 0xdc00 + (u & 0x3ff))
        }
    }
    return s
}
