/**
 * ChunkId - mirrors ndn_lib::ChunkId. Wire format is identical to ObjId
 * (`${chunk_type}:${hex(hash_result)}`); for "mix*" types the hash bytes
 * begin with a varint-encoded data length.
 */
export class ChunkId {
    public chunkType: ChunkTypeStr
    public hashResult: Uint8Array

    constructor(chunkType: ChunkTypeStr, hashResult: Uint8Array) {
        this.chunkType = chunkType
        this.hashResult = hashResult
    }

    static defaultChunkType(): ChunkTypeStr {
        return 'mix256'
    }

    static fromString(s: string): ChunkId {
        const objId = ObjId.fromString(s)
        if (!objId.isChunk()) {
            throw new NdnError('InvalidId', `invalid chunk id: ${s}`)
        }
        return new ChunkId(objId.objType, objId.objHash)
    }

    static fromObjId(objId: ObjId): ChunkId {
        return new ChunkId(objId.objType, new Uint8Array(objId.objHash))
    }

    static fromBytes(bytes: Uint8Array): ChunkId {
        const obj = ObjId.fromBytes(bytes)
        return new ChunkId(obj.objType, obj.objHash)
    }

    /** Construct from raw hash result, no length encoding. */
    static fromHashResult(hashResult: Uint8Array, chunkType: ChunkTypeStr): ChunkId {
        return new ChunkId(chunkType, new Uint8Array(hashResult))
    }

    /** Construct mix-style ChunkId by prepending varint(length) to hash. */
    static fromMixHashResult(
        dataLength: number,
        hashResult: Uint8Array,
        chunkType: ChunkTypeStr,
    ): ChunkId {
        return new ChunkId(chunkType, ChunkId.mixLengthAndHashResult(dataLength, hashResult))
    }

    static fromMixHashResultByHashMethod(
        dataLength: number,
        hashResult: Uint8Array,
        hashMethod: HashMethod,
    ): ChunkId {
        const chunkType = ChunkType.fromHashType(hashMethod, true)
        return new ChunkId(chunkType, ChunkId.mixLengthAndHashResult(dataLength, hashResult))
    }

    static fromSha256Result(hashResult: Uint8Array): ChunkId {
        return new ChunkId('sha256', new Uint8Array(hashResult))
    }

    static fromMix256Result(dataLength: number, hashResult: Uint8Array): ChunkId {
        return new ChunkId('mix256', ChunkId.mixLengthAndHashResult(dataLength, hashResult))
    }

    static mixLengthAndHashResult(dataLength: number, hashResult: Uint8Array): Uint8Array {
        const lenBytes = varintEncode(dataLength)
        const out = new Uint8Array(lenBytes.length + hashResult.length)
        out.set(lenBytes, 0)
        out.set(hashResult, lenBytes.length)
        return out
    }

    toObjId(): ObjId {
        return new ObjId(this.chunkType, new Uint8Array(this.hashResult))
    }

    toString(): string {
        return `${this.chunkType}:${bytesToHex(this.hashResult)}`
    }

    toBase32(): string {
        const typeBytes = utf8Encode(this.chunkType)
        const buf = new Uint8Array(typeBytes.length + 1 + this.hashResult.length)
        buf.set(typeBytes, 0)
        buf[typeBytes.length] = 0x3a
        buf.set(this.hashResult, typeBytes.length + 1)
        return base32Encode(buf)
    }

    toDidString(): string {
        return `did:${this.chunkType}:${bytesToHex(this.hashResult)}`
    }

    toBytes(): Uint8Array {
        return this.toObjId().toBytes()
    }

    toJSON(): string {
        return this.toString()
    }

    /** For mix-* types, return the data length encoded in the prefix. */
    getLength(): number | null {
        if (this.hashResult.length === 0) return null
        if (!ChunkType.isMix(this.chunkType)) return null
        try {
            const [len] = varintDecode(this.hashResult, 0)
            return len
        } catch {
            return null
        }
    }

    equalsHash(hashBytes: Uint8Array): boolean {
        if (this.hashResult.length !== hashBytes.length) return false
        for (let i = 0; i < this.hashResult.length; i++) {
            if (this.hashResult[i] !== hashBytes[i]) return false
        }
        return true
    }
}

